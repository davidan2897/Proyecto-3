package Domain;

/**
 *
 * @author David
 */
public class Cueva {

private int largo;
private int ancho;
private String tipo;

    public Cueva(int largo, int ancho, String tipo) {
        this.largo = largo;
        this.ancho = ancho;
        this.tipo = tipo;
    }

    public int getLargo() {
        return largo;
    }

    public void setLargo(int largo) {
        this.largo = largo;
    }

    public int getAncho() {
        return ancho;
    }

    public void setAncho(int ancho) {
        this.ancho = ancho;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    
    
}
